#define RESET "\033[0m"
#define RED(text) "\033[31m" text RESET
#define GREEN(text) "\033[32m" text RESET
#define YELLOW(text) "\033[33m" text RESET
#define BRIGHT_BLUE(text) "\033[94m" text RESET  // Bright Blue (ANSI 94)
#define VIBRANT_BLUE(text) "\033[38;5;39m" text RESET  // 256-color bright blue